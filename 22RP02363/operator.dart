void main() {
  int a = 10;
  int b = 3;

  print('Add: ${a + b}');
  print('Subtract: ${a - b}');
  print('Multiply: ${a * b}');
  print('Divide: ${a / b}');
  print('Modulus: ${a % b}');

  print('Equal: ${a == b}');
  print('Not equal: ${a != b}');
  print('Greater: ${a > b}');
  print('Less or equal: ${a <= b}');

  bool x = true;
  bool y = false;
  print('AND: ${x && y}');
  print('OR: ${x || y}');
  print('NOT: ${!x}');
}
