void main() {
  int a = 10;
  int b = 3;

  // Arithmetic operators
  print("Addition: ${a + b}");
  print("Subtraction: ${a - b}");
  print("Multiplication: ${a * b}");
  print("Division: ${a / b}");
  print("Modulus: ${a % b}");

  // Relational operators
  print("a > b: ${a > b}");
  print("a == b: ${a == b}");
  print("a != b: ${a != b}");

  // Logical operators
  bool x = true;
  bool y = false;
  print("x && y: ${x && y}");
  print("x || y: ${x || y}");
  print("!x: ${!x}");
}