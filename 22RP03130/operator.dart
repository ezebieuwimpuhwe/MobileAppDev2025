void main(){
   
  int a = 10;
  int b = 5;

  // Arithmetic
  print(a + b); // 15
  print(a - b); // 5
  print(a * b); // 50
  print(a / b); // 2.0

  // Comparison
  print(a > b);  // true
  print(a == b); // false

  // Logical
  bool x = true;
  bool y = false;
  print(x && y); // false
  print(x || y); // true
}

