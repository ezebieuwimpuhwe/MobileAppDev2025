
void main() {
  int a = 10;
  int b = 3;

  print('a + b = ${a + b}');    // 13
  print('a - b = ${a - b}');    // 7
  print('a * b = ${a * b}');    // 30
  print('a / b = ${a / b}');    // 3.333...
  print('a ~/ b = ${a ~/ b}');  // 3
  print('a % b = ${a % b}');    // 1

  print('a > b: ${a > b}');     // true
  print('a == b: ${a == b}');   // false

  bool x = true;
  bool y = false;
  print('x && y: ${x && y}');   // false
 
 

}
