void main() {
  int a = 10;
  int b = 3;

  // Arithmetic operators
  print(a + b); // 13
  print(a - b); // 7
  print(a * b); // 30
  print(a / b); // 3.333...
  print(a % b); // 1

  // Relational operators
  print(a == b); // false
  print(a != b); // true
  print(a > b);  // true
  print(a < b);  // false

  // Logical operators
  bool x = true;
  bool y = false;
  print(x && y); // false
  print(!x);     // false
}
