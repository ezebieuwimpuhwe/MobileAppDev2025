/*
Arrow Functions in Dart:

Definition:
- Concise syntax for functions with a single expression.

Syntax:
returnType functionName(params) => expression;

Usage:
- Use for simple functions to reduce code verbosity.
*/

// Example of an Arrow Function
int square(int x) => x * x;

void main() {
  print(square(4)); // Usage: calculates square of 4
}
