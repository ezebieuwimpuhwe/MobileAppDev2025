/*
Functions with Parameters in Dart:

Definition:
- Functions that accept input values to customize behavior.

Syntax:
void functionName(type param) {
  // use param
}

Usage:
- Pass data to functions to work with varying inputs.
*/

// Example of Function with Parameters
void greet(String name) {
  print("Hello, $name!");
}

void main() {
  greet("Ismael"); // Usage: passing "Ismael" as argument
}
