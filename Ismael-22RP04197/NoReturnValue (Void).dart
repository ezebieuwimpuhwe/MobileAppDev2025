/*
No Return Value (Void) in Dart:

Definition:
- Functions that do not return a value use void keyword.

Syntax:
void functionName() {
  // do something
}

Usage:
- Use when a function performs actions without returning data.
*/

// Example of Void Function
void greet() {
  print("Hello!");
}

void main() {
  greet();          // Usage: call function for side-effect (printing)
}
