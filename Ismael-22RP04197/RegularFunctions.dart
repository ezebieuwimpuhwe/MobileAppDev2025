/*
Regular Functions in Dart:

Definition:
- A block of reusable code that performs a specific task.
- Can be called multiple times.

Syntax:
void functionName() {
  // code here
}

Usage:
- Use when you want to organize code into callable units.
- Useful for tasks repeated in multiple places.
*/

// Example of a Regular Function
void greet() {
  print("Hello, World!");
}

void main() {
  greet(); // Usage: calling the function to print greeting
}
