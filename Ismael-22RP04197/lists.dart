/*
Lists in Dart:

- Ordered collection of items.
- Can store multiple values of the same type.
- Accessed by index starting at 0.
*/

// Example of a List in Dart
void main() {
  List<String> fruits = ['Apple', 'Banana', 'Orange'];  // List of strings
  print(fruits);  // Prints: [Apple, Banana, Orange]
}
