/*
Maps in Dart:

- Collection of key-value pairs.
- Keys and values can be of any type.
- Useful for storing data with a unique key for each value.
*/

// Example of a Map in Dart
void main() {
  Map<String, int> scores = {'Math': 90, 'Science': 85};  // Map with String keys and int values
  print(scores);  // Prints: {Math: 90, Science: 85}
}
