/*
Ternary Operator in Dart:

- A concise way to write simple if-else statements.
- Syntax: condition ? valueIfTrue : valueIfFalse;
- Evaluates the condition and returns one of the two values.
*/

// Example of Ternary Operator
void main() {
  int a = 5;
  String result = (a > 0) ? "Positive" : "Negative";  // If a > 0, result is "Positive", else "Negative"
  print(result);  // Prints: Positive
}
