/*
While Loop in Dart:

- Repeats a block of code while a condition is true.
- Check condition before each iteration.
*/

// Example of While Loop
void main() {
  int i = 0;
  while (i < 3) {
    print(i);  // Prints current value of i
    i++;       // Increment i by 1 each time
  }
  // Output: 0 1 2
}
